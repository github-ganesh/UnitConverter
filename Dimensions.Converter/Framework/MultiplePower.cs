





namespace Dimensions.Converter
{
   /// <summary>
   /// The powers of ten for predefined multiples
   /// </summary>
   
   public enum MultiplePower
   {
      /// <summary>
      /// Power of 0. Means factor of 1.
      /// </summary>
      /// <value>0</value>
      
      None = 0,


      /// <summary>
      /// Power of 12 to 10.
      /// </summary>
      /// <value>12</value>
      
      Tera = 12,

      /// <summary>
      /// Power of 9 to 10.
      /// </summary>
      /// <value>9</value>
      
      Giga = 9,

      /// <summary>
      /// Power of 6 to 10.
      /// </summary>
      /// <value>6</value>
      
      Mega = 6,

      /// <summary>
      /// Power of 3 to 10.
      /// </summary>
      /// <value>3</value>
      
      Kilo = 3,

      /// <summary>
      /// Power of 2 to 10.
      /// </summary>
      /// <value>2</value>
      
      Hecto = 2,

      /// <summary>
      /// Power of 1 to 10.
      /// </summary>
      /// <value>1</value>
      
      Deca = 1,

      /// <summary>
      /// Power of -1 to 10.
      /// </summary>
      /// <value>-1</value>
      
      Deci = -1,

      /// <summary>
      /// Power of -2 to 10.
      /// </summary>
      /// <value>-2</value>
      
      Centi = -2,

      /// <summary>
      /// Power of -3 to 10.
      /// </summary>
      /// <value>-3</value>
      
      Milli = -3,

      /// <summary>
      /// Power of -6 to 10.
      /// </summary>
      /// <value>-6</value>
      
      Micro = -6,

      /// <summary>
      /// Power of -9 to 10.
      /// </summary>
      /// <value>-9</value>
      
      Nano = -9,

      /// <summary>
      /// Power of -12 to 10.
      /// </summary>
      /// <value>-12</value>
      
      Pico = -12,

      /// <summary>
      /// Power of -15 to 10.
      /// </summary>
      /// <value>-15</value>
      
      Femto = -15
   }
}